  Future<void> _getCurrentLocation() async {
    try {
      print('[ChatPage] üåé Getting current device location...');
      LocationPermission permission = await Geolocator.checkPermission();
      if (permission == LocationPermission.denied) {
        permission = await Geolocator.requestPermission();
        if (permission == LocationPermission.denied) {
          print('[ChatPage] ‚ö†Ô∏è Location permission denied');
          return;
        }
      }
      
      if (permission == LocationPermission.deniedForever) {
        print('[ChatPage] ‚ö†Ô∏è Location permission permanently denied');
        return;
      }
      
      Position position = await Geolocator.getCurrentPosition(
        desiredAccuracy: LocationAccuracy.high
      );
      
      setState(() {
        _latitude = position.latitude;
        _longitude = position.longitude;
      });
      
      print('[ChatPage] üìç Current location: lat=${_latitude}, lng=${_longitude}');
    } catch (e) {
      print('[ChatPage] ‚ö†Ô∏è Error getting location: $e');
